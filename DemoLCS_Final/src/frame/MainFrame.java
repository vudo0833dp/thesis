    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: Button for Start
        if(!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected())
        {
            JOptionPane.showMessageDialog(null, "Choose one option before STARTING!");
        }else{
            if(jRadioButton1.isSelected()) {
       
                JFrame st_frame = new SuffixTreeFrame();
                st_frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                st_frame.setTitle("Suffix Tree");
                st_frame.setVisible(true);
            }
            else if(jRadioButton2.isSelected()){
                JFrame sa_frame = new SuffixArrayFrame();
                sa_frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                sa_frame.setTitle("Suffix Array");
                sa_frame.setVisible(true);
            }
            else {
                JFrame dp = new DynamicProgramming();
                dp.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                dp.setTitle("Dynamic Programing");
                dp.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

