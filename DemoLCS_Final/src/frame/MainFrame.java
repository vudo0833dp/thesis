    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // This is button start of Frame Application
        
        // This is checking condition in STEP 1
        String messageToShow = "";
        String numberOfPAA = jTextField1.getText();
        int position = jComboBox1.getSelectedIndex();
        if(position == 0) messageToShow += "- You haven't choosen algorithms. \n";
        if(list_File == null){
            messageToShow += "- You haven't choosen any input files. \n";
        }
        else if(list_File.length > 0 && list_File.length != 2) messageToShow += "- Number of inputs file is NOT 2. \n";
        
        // This is checking condition in STEP 2
        if(!jRadioButton4.isSelected() && !jRadioButton5.isSelected() && !jRadioButton6.isSelected()
                && !jRadioButton7.isSelected()){
            if(!jRadioButton8.isSelected()){
                messageToShow += "- You haven't choosen number of PAA. \n";
            } else{
                if(numberOfPAA.equals("")){
                    messageToShow += "- You haven't fill number of PAA into the INPUT FIELD. \n";
                }
            }
        }
        
        if(!messageToShow.equals("")){
            JOptionPane.showMessageDialog(null, "CANNOT START BECAUSE: \n" +messageToShow);
        } else {
            int paaValue = 0;
            if(jRadioButton4.isSelected()) paaValue = 50;
            else if(jRadioButton5.isSelected()) paaValue = 75;
            else if(jRadioButton6.isSelected()) paaValue = 100;
            else if(jRadioButton7.isSelected()) paaValue = 125;
            else paaValue = Integer.parseInt(jTextField1.getText());
            
            // Starting SAX
            SAX sax = new SAX();
            sax.buildSAX_Symbols(position, list_File, paaValue);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

